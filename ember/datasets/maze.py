"""Maze domain: fixed-size grid with random obstacles, start at top-left,
goal at bottom-right, ground-truth solution is the BFS shortest path.

Mazes are generated by sampling a random obstacle grid and retrying until a
start-goal path exists (falling back to an open grid after too many failed
attempts), then computing the shortest path with breadth-first search. This
is the simple "obstacle gridworld" style of maze used throughout pathfinding
and RL research, not a perfect (uniquely-connected) maze -- shortest paths
are not required to be unique.
"""

from __future__ import annotations

import random
from collections import deque

import torch

from ember.datasets.domain import Domain, ProblemInstance, register_domain

Cell = tuple[int, int]
WallGrid = list[list[int]]


@register_domain
class MazeDomain(Domain):
    name = "maze"

    def __init__(self, height: int = 7, width: int = 7, wall_prob: float = 0.25) -> None:
        self.h = height
        self.w = width
        self.wall_prob = wall_prob
        self.start: Cell = (0, 0)
        self.goal: Cell = (height - 1, width - 1)
        self.problem_dim = 3 * height * width  # wall / start / goal channels
        self.solution_dim = height * width  # path membership mask

    def generate(self, n: int, seed: int) -> list[ProblemInstance]:
        rng = random.Random(seed)
        instances = []
        for _ in range(n):
            path = None
            walls: WallGrid = []
            attempts = 0
            while path is None and attempts < 200:
                attempts += 1
                walls = self._random_grid(rng)
                path = self._bfs_shortest_path(walls, self.start, self.goal)
            if path is None:
                walls = [[0] * self.w for _ in range(self.h)]
                path = self._bfs_shortest_path(walls, self.start, self.goal)
            problem = {"walls": walls, "start": list(self.start), "goal": list(self.goal)}
            instances.append(
                ProblemInstance(problem=problem, solution=path, meta={"path_len": len(path)})
            )
        return instances

    def _random_grid(self, rng: random.Random) -> WallGrid:
        walls = [
            [1 if rng.random() < self.wall_prob else 0 for _ in range(self.w)]
            for _ in range(self.h)
        ]
        walls[self.start[0]][self.start[1]] = 0
        walls[self.goal[0]][self.goal[1]] = 0
        return walls

    def _bfs_shortest_path(self, walls: WallGrid, start: Cell, goal: Cell) -> list[Cell] | None:
        prev: dict[Cell, Cell | None] = {start: None}
        q: deque[Cell] = deque([start])
        while q:
            cur = q.popleft()
            if cur == goal:
                break
            r, c = cur
            for dr, dc in ((-1, 0), (1, 0), (0, -1), (0, 1)):
                nr, nc = r + dr, c + dc
                if (
                    0 <= nr < self.h
                    and 0 <= nc < self.w
                    and walls[nr][nc] == 0
                    and (nr, nc) not in prev
                ):
                    prev[(nr, nc)] = cur
                    q.append((nr, nc))
        if goal not in prev:
            return None
        path = []
        cur: Cell | None = goal
        while cur is not None:
            path.append(cur)
            cur = prev[cur]
        path.reverse()
        return path

    def encode_problem(self, problem: dict) -> torch.Tensor:
        walls = torch.tensor(problem["walls"], dtype=torch.float32)
        start_ch = torch.zeros(self.h, self.w)
        goal_ch = torch.zeros(self.h, self.w)
        sr, sc = problem["start"]
        gr, gc = problem["goal"]
        start_ch[sr, sc] = 1.0
        goal_ch[gr, gc] = 1.0
        return torch.stack([walls, start_ch, goal_ch]).reshape(-1)

    def encode_solution(self, solution: list[Cell]) -> torch.Tensor:
        mask = torch.zeros(self.h, self.w)
        for r, c in solution:
            mask[r, c] = 1.0
        return mask.reshape(-1)

    def decode_solution(self, problem: dict, tensor: torch.Tensor) -> list[Cell]:
        mask = tensor.reshape(self.h, self.w)
        return [(r, c) for r in range(self.h) for c in range(self.w) if mask[r, c].item() > 0.5]

    def verify(self, problem: dict, solution: list[Cell]) -> tuple[bool, int]:
        walls = problem["walls"]
        start, goal = tuple(problem["start"]), tuple(problem["goal"])
        cells = set(tuple(c) for c in solution)
        violations = sum(1 for (r, c) in cells if walls[r][c] == 1)
        if start not in cells:
            violations += 1
        if goal not in cells:
            violations += 1

        open_cells = {c for c in cells if walls[c[0]][c[1]] == 0}
        if start in open_cells and goal in open_cells:
            visited = {start}
            q = deque([start])
            while q:
                r, c = q.popleft()
                for dr, dc in ((-1, 0), (1, 0), (0, -1), (0, 1)):
                    nb = (r + dr, c + dc)
                    if nb in open_cells and nb not in visited:
                        visited.add(nb)
                        q.append(nb)
            if goal not in visited:
                violations += 1
        else:
            violations += 1
        return violations == 0, violations

    def random_solution(
        self, problem: dict, generator: torch.Generator | None = None
    ) -> torch.Tensor:
        return torch.randn(self.solution_dim, generator=generator)

    def perturb(
        self,
        solution_tensor: torch.Tensor,
        noise: float = 0.5,
        generator: torch.Generator | None = None,
    ) -> torch.Tensor:
        x = solution_tensor.clone()
        n_flip = max(1, int(0.15 * self.solution_dim))
        idx = torch.randperm(self.solution_dim, generator=generator)[:n_flip]
        x[idx] = -x[idx]
        x = x + noise * 0.3 * torch.randn(x.shape, generator=generator)
        return x.clamp(-3.0, 3.0)
