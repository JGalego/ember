"""Sudoku domain: 9x9 grid, digits 1-9, standard row/column/box constraints.

Puzzles are generated by building a full valid grid via randomized
backtracking (diagonal boxes filled first, since they're mutually
independent, which speeds up the search considerably) and then removing a
fixed number of cells. Uniqueness of the resulting puzzle is NOT verified
(that requires a second, counting solve pass) -- this is a research dataset
generator, not a puzzle-publishing pipeline, and difficulty is only
approximate via `n_remove`.
"""

from __future__ import annotations

import random

import torch
import torch.nn.functional as F

from ember.datasets.domain import (
    Domain,
    ProblemInstance,
    randn_like,
    randperm_like,
    register_domain,
)

Grid = list[list[int]]


def _is_valid(grid: Grid, r: int, c: int, val: int) -> bool:
    if any(grid[r][cc] == val for cc in range(9)):
        return False
    if any(grid[rr][c] == val for rr in range(9)):
        return False
    br, bc = 3 * (r // 3), 3 * (c // 3)
    for rr in range(br, br + 3):
        for cc in range(bc, bc + 3):
            if grid[rr][cc] == val:
                return False
    return True


def _find_empty(grid: Grid) -> tuple[int, int] | None:
    for r in range(9):
        for c in range(9):
            if grid[r][c] == 0:
                return r, c
    return None


def _solve_backtrack(grid: Grid, rng: random.Random) -> bool:
    empty = _find_empty(grid)
    if empty is None:
        return True
    r, c = empty
    nums = list(range(1, 10))
    rng.shuffle(nums)
    for val in nums:
        if _is_valid(grid, r, c, val):
            grid[r][c] = val
            if _solve_backtrack(grid, rng):
                return True
            grid[r][c] = 0
    return False


def generate_full_grid(rng: random.Random) -> Grid:
    """A complete, valid 9x9 Sudoku solution grid."""
    grid = [[0] * 9 for _ in range(9)]
    for box in range(3):
        nums = list(range(1, 10))
        rng.shuffle(nums)
        for i in range(3):
            for j in range(3):
                grid[box * 3 + i][box * 3 + j] = nums.pop()
    ok = _solve_backtrack(grid, rng)
    assert ok, "backtracking failed to complete a full grid (should not happen)"
    return grid


def make_puzzle(full_grid: Grid, rng: random.Random, n_remove: int) -> Grid:
    puzzle = [row[:] for row in full_grid]
    cells = [(r, c) for r in range(9) for c in range(9)]
    rng.shuffle(cells)
    for r, c in cells[:n_remove]:
        puzzle[r][c] = 0
    return puzzle


def count_duplicates(values: list[int]) -> int:
    seen: dict[int, int] = {}
    for v in values:
        seen[v] = seen.get(v, 0) + 1
    return sum(cnt - 1 for cnt in seen.values() if cnt > 1)


@register_domain
class SudokuDomain(Domain):
    name = "sudoku"

    def __init__(self, n_remove: int = 45) -> None:
        self.n_remove = n_remove
        self.problem_dim = 9 * 9 * 10  # one-hot(0..9) per cell, 0 = blank
        self.solution_dim = 9 * 9 * 9  # one-hot(1..9) per cell

    def generate(self, n: int, seed: int) -> list[ProblemInstance]:
        rng = random.Random(seed)
        instances = []
        for _ in range(n):
            full = generate_full_grid(rng)
            puzzle = make_puzzle(full, rng, self.n_remove)
            n_given = sum(1 for row in puzzle for v in row if v != 0)
            instances.append(
                ProblemInstance(problem=puzzle, solution=full, meta={"n_given": n_given})
            )
        return instances

    def encode_problem(self, problem: Grid) -> torch.Tensor:
        grid = torch.tensor(problem, dtype=torch.long)
        return F.one_hot(grid, num_classes=10).float().reshape(-1)

    def encode_solution(self, solution: Grid) -> torch.Tensor:
        grid = torch.tensor(solution, dtype=torch.long) - 1
        return F.one_hot(grid, num_classes=9).float().reshape(-1)

    def decode_solution(self, problem: Grid, tensor: torch.Tensor) -> Grid:
        logits = tensor.reshape(81, 9)
        digits = (logits.argmax(dim=-1) + 1).reshape(9, 9)
        return digits.tolist()

    def verify(self, problem: Grid, solution: Grid) -> tuple[bool, int]:
        violations = 0
        for r in range(9):
            for c in range(9):
                given = problem[r][c]
                if given != 0 and solution[r][c] != given:
                    violations += 1
        for r in range(9):
            violations += count_duplicates(solution[r])
        for c in range(9):
            violations += count_duplicates([solution[r][c] for r in range(9)])
        for br in range(3):
            for bc in range(3):
                vals = [
                    solution[r][c]
                    for r in range(br * 3, br * 3 + 3)
                    for c in range(bc * 3, bc * 3 + 3)
                ]
                violations += count_duplicates(vals)
        return violations == 0, violations

    def random_solution(
        self, problem: Grid, generator: torch.Generator | None = None
    ) -> torch.Tensor:
        return torch.randn(self.solution_dim, generator=generator)

    def perturb(
        self,
        solution_tensor: torch.Tensor,
        noise: float = 0.5,
        generator: torch.Generator | None = None,
    ) -> torch.Tensor:
        x = solution_tensor.clone().reshape(81, 9)
        n_swap = max(2, int(0.15 * 81))
        idx = randperm_like(81, x, generator=generator)[:n_swap]
        perm = idx[randperm_like(len(idx), x, generator=generator)]
        x[idx] = x[perm].clone()
        x = x + noise * 0.3 * randn_like(x, generator=generator)
        return x.reshape(-1)
